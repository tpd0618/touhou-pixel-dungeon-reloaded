import java.util.ArrayList;

public class Renko extends Mob implements Callback {

    private static final float TIME_TO_ZAP	= 1f;

    {
        spriteClass = RenkoSprite.class;

        HP = HT = 229;
        defenseSkill = 37;
        EXP = 22;
        maxLvl = 75;

        properties.add(Property.HUMAN);
        properties.add(Property.HARASSMENT);

        properties.add(Property.FUMO);
        //used for fumo lover buff

        loot = new LifeFragment();
        lootChance = 0.1f;
    }

    @Override
    public int damageRoll() {
        return Random.NormalIntRange(59, 91);
    }

    @Override
    public int attackSkill(Char target) {
        return 50;
    }

    @Override
    public int drRoll() {
        return Random.NormalIntRange(31, 44);
    }

    @Override
    protected boolean canAttack( Char enemy ) {
        return new Ballistica( pos, enemy.pos, Ballistica.MAGIC_BOLT).collisionPos == enemy.pos;
    }

    protected boolean doAttack( Char enemy ) {

        if (Dungeon.level.adjacent( pos, enemy.pos )) {

            return super.doAttack( enemy );

        } else {

            if (sprite != null && (sprite.visible || enemy.sprite.visible)) {
                sprite.zap( enemy.pos );
                return false;
            } else {
                zap();
                return true;
            }
        }
    }

    //used so resistances can differentiate between melee and magical attacks
    public static class DarkBolt{}

    protected void zap() {
        spend( TIME_TO_ZAP );

        if (hit( this, enemy, true )) {
            //TODO would be nice for this to work on ghost/statues too
            if (enemy == Dungeon.heroine && enemy.alignment != this.alignment) {
                if (Statistics.difficulty > 2) {
                    Buff.prolong(enemy, MagicDrain.class, MagicDrain.DURATION);
                }
                if (Statistics.difficulty > 4) {
                    Buff.prolong(enemy, CursedBlow.class, CursedBlow.DURATION);
                }
                switch (Random.Int(5)) {
                    case 0:
                    default:
                        if (enemy.HP > 4) {
                            enemy.HP = enemy.HP / 4;
                            Sample.INSTANCE.play(Assets.Sounds.CURSED);
                            CellEmitter.get(enemy.pos).burst(ShadowParticle.UP, 5);
                            GLog.w(Messages.get(this, "super_torment"));
                        }
                        break;
                    case 1:
                        Buff.prolong(enemy, Slow.class, Slow.DURATION);
                        break;
                    case 2:
                        Buff.prolong(enemy, Onigiri.class, Onigiri.DURATION);
                        break;
                    case 3:
                        Buff.prolong(enemy, Randomizer.class, Randomizer.DURATION);
                        break;
                    case 4:
                        Buff.prolong(enemy, Empathetic.class, Empathetic.DURATION);
                        break;
                }
            }

            if (enemy == Dungeon.heroine && !enemy.isAlive()) {
                Dungeon.fail( getClass() );
                GLog.n( Messages.get(this, "bolt_kill") );
            }
        } else {
            enemy.sprite.showStatus( CharSprite.NEUTRAL,  enemy.defenseVerb() );
        }
    }

    public void onZapComplete() {
        zap();
        next();
    }

    @Override
    public void call() {
        next();
    }
}